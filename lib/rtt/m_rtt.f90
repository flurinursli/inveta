      REAL(r64),              INTENT(INOUT) :: tictoc                          !< elapsed time
      INTEGER(i32), OPTIONAL, INTENT(IN)    :: comm                            !< communicator handle
      INTEGER(i32)                          :: count, rate, ierr

      !-----------------------------------------------------------------------------------------------------------------------------

