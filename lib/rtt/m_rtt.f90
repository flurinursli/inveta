    REAL(r64) FUNCTION rms(r)

      ! Purpose:
      !   To compute the root-mean-square of input vector "r" based on the compensated-summation version of the two-pass algorithm.
      !
      ! Revisions:
      !     Date                    Description of change
      !     ====                    =====================
      !   04/05/20                  original version
      !

      REAL(r64),   DIMENSION(:), INTENT(IN) :: r
      INTEGER(i32)                          :: i, n
      REAL(r64)                             :: mu, s1, s2, x, v, c

      !---------------------------------------------------------------------------------------------------------------------------

      n = SIZE(r)

      ! compute mean
      mu = 0._r64
      v  = 1._r64

      DO i = 1, n
        mu = mu + (r(i) - mu) / v
        v  = v + 1._r64
      ENDDO

      ! compute variance
      s1 = 0._r64
      s2 = 0._r64

      DO i = 1, n
        x = r(i) - mu
        s1 = s1 + x
        s2 = s2 + x**2
      ENDDO

      s1 = (s1**2) / n

      v = (s2 - s1) / (n - 1)

      rms = SQRT(v + mu**2)

    END FUNCTION rms

